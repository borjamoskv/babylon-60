"""
Benchmark Git KV-DAG vs SQLite WAL under extreme load.
"""

import time
import asyncio
from cortex.engine.gaad.kv_dag import GaaDKVDAG
from cortex.engine.storage_router import StorageRouter

def test_gaad_o1_resolution():
    router = StorageRouter(use_gaad=True)
    
    # Generate 1000 facts
    facts = [f"Fact #{i} generated by C5-REAL Swarm.".encode("utf-8") for i in range(1000)]
    
    # 1. Benchmark GaaD writes
    start = time.time()
    hashes = []
    for fact in facts:
        h = router.gaad.put_blob(fact)
        hashes.append(h)
    write_time = time.time() - start
    
    # 2. Benchmark GaaD reads
    start = time.time()
    for h in hashes:
        content = router.gaad.get_blob(h)
        assert content is not None
    read_time = time.time() - start
    
    print("========================================")
    print("🚀 GaaD Benchmark (1000 operations)")
    print("========================================")
    print(f"Write Time: {write_time:.4f}s")
    print(f"Read Time:  {read_time:.4f}s")
    print("========================================")

if __name__ == "__main__":
    test_gaad_o1_resolution()
