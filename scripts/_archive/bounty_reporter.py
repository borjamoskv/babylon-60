import os
from datetime import datetime


class BountyReporter:
    """
    CORTEX Bounty Reporter: Professional Evidence Synthesis.
    Generates high-exergy reports for platform submission.
    """
    def __init__(self, program_id):
        self.program_id = program_id
        self.report_dir = "reports"
        os.makedirs(self.report_dir, exist_ok=True)

    def generate_report(self, title, description, impact, reproduction_steps):
        timestamp = datetime.now().strftime("%Y-%m-%d_%H%M%S")
        report_id = f"CORTEX-{self.program_id.upper()}-{timestamp}"
        filename = os.path.join(self.report_dir, f"REPORT_{report_id}.md")
        
        content = f"""# Bug Bounty Report: {title}
**Report ID**: {report_id}
**Program**: {self.program_id}
**Date**: {timestamp}
**Status**: DRAFT (Awaiting Final Human Sign-off)

---

## 1. Executive Summary
{description}

## 2. Technical Impact Analysis
{impact}

## 3. Reproduction Steps (CORTEX Automated Validation)
{reproduction_steps}

## 4. Remediation Recommendations
- Implement strict bounds-checking on the affected logic.
- Audit adjacent modules for similar edge-case vulnerabilities.

---
*Generated by CORTEX Bounty Operator v1.0 | Industrial Noir 2026*
"""
        with open(filename, 'w') as f:
            f.write(content)
            
        print(f">> [CORTEX-SUCCESS] Bounty Report generated: {filename}")
        return filename

