"""GPU-accelerated JIT concept formation.

Mathematical concepts generated by PeARL induction are projected
into tensor space on the available accelerator (MPS/CUDA/CPU)
and crystallized as KNOWLEDGE transactions in the dual ledger.
"""

from __future__ import annotations

import logging
import time
from dataclasses import dataclass
from typing import Any

try:
    import torch
except ImportError:  # pragma: no cover
    torch = None  # type: ignore[assignment]

from cortex.autodidact.dual_ledger import DualLedger
from cortex.engine.pearl import PearlEngine
from cortex.utils.errors import PearlError

__all__ = ["JITSynthesizer", "SynthesisResult"]

logger = logging.getLogger("cortex.autodidact.jit_synthesizer")


def _resolve_device() -> Any:
    """Pick the best available accelerator."""
    if torch is None:
        return None
    if hasattr(torch.backends, "mps") and torch.backends.mps.is_available():
        return torch.device("mps")
    if torch.cuda.is_available():
        return torch.device("cuda")
    return torch.device("cpu")


@dataclass(frozen=True)
class SynthesisResult:
    """Outcome of a JIT concept synthesis."""

    success: bool
    tx_hash: str = ""
    concept_name: str = ""
    device: str = "cpu"
    elapsed_ms: float = 0.0
    tensor_norm: float = 0.0
    error: str = ""


class JITSynthesizer:
    """GPU JIT concept formation engine (AX-046).

    Flow
    ----
    1. Evaluate PeARL expression in sandbox.
    2. Project the scalar/vector result into a torch tensor on GPU.
    3. Compute tensor norm as a concept "signature".
    4. Crystallize via DualLedger as a KNOWLEDGE transaction.

    Parameters
    ----------
    pearl : PearlEngine
        PeARL primitive induction engine.
    ledger : DualLedger
        Unified dual-stream hash-chain.
    device : str
        Force device ("cpu", "mps", "cuda") or "auto".
    """

    def __init__(
        self,
        pearl: PearlEngine,
        ledger: DualLedger,
        device: str = "auto",
    ) -> None:
        self.pearl = pearl
        self.ledger = ledger

        if torch is None:
            self._device = None
            logger.warning("torch not available; JIT synthesis will run in scalar mode.")
        elif device == "auto":
            self._device = _resolve_device()
        else:
            self._device = torch.device(device)

        if self._device is not None:
            logger.info("JIT Synthesizer initialized on %s", self._device)

    @property
    def device_name(self) -> str:
        return str(self._device) if self._device else "scalar"

    def _to_tensor(self, value: Any) -> Any:
        """Convert a PeARL output to a torch tensor."""
        if torch is None or self._device is None:
            return value  # Scalar fallback

        if isinstance(value, (int, float)):
            return torch.tensor([float(value)], device=self._device)
        if isinstance(value, list):
            # Flatten nested lists (grids) to 1-D
            flat: list[float] = []
            for item in value:
                if isinstance(item, list):
                    flat.extend(float(v) for v in item)
                else:
                    flat.append(float(item))
            return torch.tensor(flat, device=self._device)
        return torch.tensor([float(value)], device=self._device)

    async def synthesize(
        self,
        concept_name: str,
        pearl_expr: str,
        context: dict[str, Any] | None = None,
        *,
        tenant_id: str = "default",
    ) -> SynthesisResult:
        """Evaluate, project, and crystallize a concept.

        Parameters
        ----------
        concept_name : str
            Human-readable identifier for the concept.
        pearl_expr : str
            PeARL expression to evaluate.
        context : dict, optional
            Variable bindings for the PeARL evaluation.
        """
        t0 = time.perf_counter()

        # 1. PeARL sandbox evaluation
        try:
            raw_result = self.pearl.evaluate(pearl_expr, dict(context or {}))
        except PearlError as exc:
            return SynthesisResult(
                success=False,
                concept_name=concept_name,
                device=self.device_name,
                error=str(exc),
                elapsed_ms=(time.perf_counter() - t0) * 1000,
            )

        # 2. Tensor projection
        tensor = self._to_tensor(raw_result)
        if torch is not None and isinstance(tensor, torch.Tensor):
            norm = float(tensor.norm().item())
        else:
            norm = float(abs(tensor)) if isinstance(tensor, (int, float)) else 0.0

        # 3. Crystallize as KNOWLEDGE tx
        detail: dict[str, Any] = {
            "concept": concept_name,
            "pearl_expr": pearl_expr,
            "tensor_norm": norm,
            "device": self.device_name,
        }

        tx_hash = await self.ledger.record_knowledge(
            project="millennium",
            action="jit_concept_synthesized",
            detail=detail,
            tenant_id=tenant_id,
            skip_cache=True,
        )

        elapsed = (time.perf_counter() - t0) * 1000

        logger.info(
            "JIT-SYN [%s] norm=%.4f device=%s %.1fms → %s",
            concept_name,
            norm,
            self.device_name,
            elapsed,
            tx_hash[:12],
        )

        return SynthesisResult(
            success=True,
            tx_hash=tx_hash,
            concept_name=concept_name,
            device=self.device_name,
            elapsed_ms=elapsed,
            tensor_norm=norm,
        )
