"""
Zero-Knowledge Gatekeeper (Fase 7).
Acts as the ultimate Execution Firewall between Swarm consensus and the Host OS.
"""

import hashlib
import logging
from typing import Any

from cortex.crypto.keys import KeyManager, Verifier

logger = logging.getLogger(__name__)


class SecurityViolationError(Exception):
    """Raised when an execution request lacks valid cryptographic PoQC (Proof of Quality Consensus)."""
    pass


class ZeroKnowledgeGatekeeper:
    """
    Execution Firewall. Prevents agent hallucination or malicious OS mutations
    by demanding a strict cryptographic signature from the ByzantineJudge before
    execution on the Host.
    """

    def __init__(self, km: KeyManager = None):  # type: ignore
        self.km = km or KeyManager(service_name="cortex_swarm_judge")

    def execute_consensus(self, consensus_proof: dict[str, Any], dry_run: bool = False) -> bool:
        """
        Receives a ConsensusProof dictionary generated by the ByzantineJudge:
        {
            "winning_agent": "...",
            "ast_code": "...",
            "consensus_timestamp": "...",
            "consensus_signature_b64": "...",
            "judge_id": "byzantine_judge_root"
        }
        
        If verified, the Gatekeeper simulates or executes the AST on the OS.
        """
        judge_id = consensus_proof.get("judge_id")
        ast_code = consensus_proof.get("ast_code")
        timestamp = consensus_proof.get("consensus_timestamp")
        signature_b64 = consensus_proof.get("consensus_signature_b64")

        if not all([judge_id, ast_code, timestamp, signature_b64]):
            logger.critical("[P0_ABORT] Incomplete ConsensusProof submitted to Gatekeeper.")
            raise SecurityViolationError("Gatekeeper blocked execution: Incomplete Consensus Proof.")

        # 1. Fetch the Judge's Public Key
        judge_pub_key_b64 = self.km.get_public_key_b64(judge_id)  # type: ignore
        if not judge_pub_key_b64:
            logger.critical(f"[P0_ABORT] Unknown Judge Identity: {judge_id}.")
            raise SecurityViolationError(f"Gatekeeper blocked execution: Unknown Judge {judge_id}.")

        # 2. Cryptographic Verification
        payload_hash = hashlib.sha256(ast_code.encode("utf-8")).hexdigest()  # type: ignore
        try:
            is_valid = Verifier.verify_signature(judge_pub_key_b64, payload_hash, timestamp, signature_b64)  # type: ignore
        except Exception as e:
            logger.error(f"Gatekeeper crypto failure: {e}")
            is_valid = False

        if not is_valid:
            logger.critical("[P0_ABORT] APOPTOSIS DETONATED. Gatekeeper blocked forged execution request.")
            raise SecurityViolationError("Gatekeeper blocked execution: Cryptographic forgery detected.")

        # 3. Safe Execution Context (Taint-Token approved)
        logger.info(f"Gatekeeper unlocked for verified AST from {judge_id}.")
        
        if dry_run:
            logger.info("[Gatekeeper] DRY_RUN Mode: OS execution bypassed.")
            return True

        # In a real system, here we would inject the AST into the target Python file
        # or execute the side-effect via OS API.
        try:
            # Dangerous execution inside safe zone. We compile to ensure it's valid python at least.
            compile(ast_code, "<gatekeeper_exec>", "exec")  # type: ignore
            # We don't actually run arbitrary untrusted AST here in the main thread of CORTEX
            # It should be written to the workspace.
            logger.info("Gatekeeper execution successful (Code structurally valid).")
            return True
        except Exception as e:
            logger.error(f"Gatekeeper execution failed post-consensus: {e}")
            return False
