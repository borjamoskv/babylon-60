import json
import os
import sys


def generate_lean_checker(graph_json_path: str, output_lean_path: str):
    """
    Lee un grafo desde JSON y genera un comprobador de colorabilidad en Lean 4
    desenrollando los bucles y aplicando podas estáticas (Fast Pruning).
    """
    if not os.path.exists(graph_json_path):
        print(f"Error: {graph_json_path} no encontrado.")
        sys.exit(1)

    with open(graph_json_path) as f:
        data = json.load(f)

    n = data["n"]
    k = data["k"]
    edges = data["edges"]  # Lista de [u, v]

    # Pre-calcular adyacencias
    adj = {i: [] for i in range(n)}
    for u, v in edges:
        adj[u].append(v)
        adj[v].append(u)

    lean_code = [
        "-- [C5-REAL] Fast 3-Colorability Checker",
        "-- Auto-generated by generate_lean_proof.py",
        "def is_colorable_hard_graph : Bool := ",
        f"  let k := {k}",
    ]

    indent = "  "
    for i in range(n):
        lean_code.append(f"{indent}List.any (List.range k) fun c{i} =>")
        indent += "  "
        # Prune condition: current node color must not match previously assigned adjacent nodes
        prunes = []
        for neighbor in adj[i]:
            if neighbor < i:  # Already assigned
                prunes.append(f"c{i} == c{neighbor}")
        if prunes:
            condition = " || ".join(prunes)
            lean_code.append(f"{indent}if {condition} then false else")
            indent += "  "

    # If all nested loops complete without failing pruning, we found a valid coloring
    lean_code.append(f"{indent}true")

    # The proof
    lean_code.append("")
    lean_code.append(
        "-- Teorema formal: Computar `is_colorable_hard_graph` demuestra exhaustivamente que es falso."
    )
    lean_code.append("theorem hard_graph_is_unsat : is_colorable_hard_graph = false := by rfl")

    with open(output_lean_path, "w") as f:
        f.write("\n".join(lean_code) + "\n")

    print(f"Lean 4 checker exportado en {output_lean_path}")
    print(f"El grafo tiene {n} nodos y {len(edges)} aristas. Poda estática generada.")


if __name__ == "__main__":
    base_dir = os.path.dirname(__file__)
    json_path = os.path.join(base_dir, "hard_graph.json")
    lean_path = os.path.join(
        base_dir, "cortex_math_lean", "CortexMathLean", "HardGraphChecker.lean"
    )
    generate_lean_checker(json_path, lean_path)
