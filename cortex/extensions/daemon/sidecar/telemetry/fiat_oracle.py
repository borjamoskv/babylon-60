import asyncio
import hashlib
import json
import logging
import random
import time
from pathlib import Path
from typing import Any, Final

from cortex.utils.errors import CortexError

logger = logging.getLogger(__name__)

# --- Sovereign Constants ---
MAX_RETRIES: Final[int] = 3
BASE_BACKOFF: Final[float] = 1.1


class FiatOracle:
    """Monitors fiat flow and emits telemetry events. (Zero-Trust)"""

    def __init__(self, engine: Any, interval: float = 30.0):
        # We accept both sync and async engine for flexibility
        self.engine = engine
        self.interval = interval
        self.running = False

        # Operation Citadel: Persistent Queue approach (No Single File Spoilage)
        self.queue_dir = Path("~/.cortex/fiat_queue").expanduser()
        self.queue_dir.mkdir(parents=True, exist_ok=True)

        self.processed_txs: set[str] = set()  # Ephemeral Idempotency memory

    async def run_loop(self):
        """Main async loop for the sidecar."""
        self.running = True
        logger.info("💸 [FIAT_ORACLE] Activated. Monitoring sovereign capital flow (DEFCON 1).")

        while self.running:
            try:
                await self._check_signals()
            except (OSError, ValueError, CortexError) as e:
                logger.error("❌ [FIAT_ORACLE] Error: %s", e)
            await asyncio.sleep(self.interval)

    def run_sync_loop(self):
        """For running in MoskvDaemon separate thread."""
        logger.info("💸 [FIAT_ORACLE] (Thread) started.")
        self.running = True
        while self.running:
            try:
                # We use sync checks here if called from a thread
                self._check_signals_sync()
            except (OSError, ValueError, CortexError) as e:
                logger.error("❌ [FIAT_ORACLE] (Thread) Error: %s", e)
            time.sleep(self.interval)

    def _verify_signature(self, data: dict, signature: str) -> bool:
        """
        Anti-Spoofing: Verifies that the payload was generated by a trusted source.
        Uses a dummy SOVEREIGN_KEY_MOCK for architectural simulation.
        """
        if not signature:
            return False

        # Reconstruct payload to compute hash (excluding the signature itself)
        # Note: In real life we'd use hmac and a real env variable.
        payload_copy = {k: v for k, v in data.items() if k != "signature"}
        payload_str = json.dumps(payload_copy)

        expected_hash = hashlib.sha256((payload_str + "SOVEREIGN_KEY_MOCK").encode()).hexdigest()
        return signature == expected_hash

    async def _check_signals(self):
        """Check for simulated transactions in the persistent queue."""

        def _get_tx_files() -> list[Path]:
            return list(self.queue_dir.glob("*.json"))

        tx_files = await asyncio.to_thread(_get_tx_files)
        for tx_file in tx_files:
            try:
                content = await asyncio.to_thread(tx_file.read_text)
                data = json.loads(content)

                # 1. Zero-Trust Validation
                signature = data.get("signature")
                if not self._verify_signature(data, signature):
                    logger.critical(
                        "🚨 [FIAT_ORACLE] Firma inválida rechazada. Posible Spoofing: %s",
                        tx_file.name,
                    )
                    await asyncio.to_thread(tx_file.unlink)  # Delete malicious file immediately
                    continue

                # 2. Idempotency Check
                tx_id = data.get("tx_id")
                if tx_id in self.processed_txs:
                    logger.warning("🛡️ [FIAT_ORACLE] Prevented replay attack para TX: %s", tx_id)
                    await asyncio.to_thread(tx_file.unlink)
                    continue

                # 3. Process & Commit
                await self._process_transaction(data)

                # 4. Finalize
                self.processed_txs.add(tx_id)
                await asyncio.to_thread(tx_file.unlink)  # Cleanup only after successful commit

            except json.JSONDecodeError:
                logger.error("❌ [FIAT_ORACLE] Payload corrupto en %s", tx_file.name)
                try:
                    await asyncio.to_thread(tx_file.unlink)
                except OSError:
                    pass
            except (OSError, ValueError, CortexError, KeyError) as e:
                logger.error("⚠️ [FIAT_ORACLE] Falla procesando %s: %s", tx_file.name, e)

    def _check_signals_sync(self):
        """Sync version for threaded execution."""
        for tx_file in self.queue_dir.glob("*.json"):
            try:
                content = tx_file.read_text()
                data = json.loads(content)

                # 1. Zero-Trust Validation
                signature = data.get("signature")
                if not self._verify_signature(data, signature):
                    logger.critical(
                        "🚨 [FIAT_ORACLE] Firma inválida rechazada. Posible Spoofing: %s",
                        tx_file.name,
                    )
                    tx_file.unlink()
                    continue

                # 2. Idempotency Check
                tx_id = data.get("tx_id")
                if tx_id in self.processed_txs:
                    logger.warning("🛡️ [FIAT_ORACLE] Prevented replay attack para TX: %s", tx_id)
                    tx_file.unlink()
                    continue

                # 3. Process & Commit
                self._process_transaction_sync(data)

                # 4. Finalize
                self.processed_txs.add(tx_id)
                tx_file.unlink()

            except json.JSONDecodeError:
                logger.error("❌ [FIAT_ORACLE] Payload corrupto en %s", tx_file.name)
                tx_file.unlink()
            except (OSError, ValueError, CortexError, KeyError) as e:
                logger.error("⚠️ [FIAT_ORACLE] Falla procesando %s: %s", tx_file.name, e)

    async def _execute_with_backoff_async(self, payload: dict[str, Any]) -> None:
        """Resilient storage with exponential backoff avoiding 'Database is locked' errors (Async)."""
        amount = payload.get("amount", "0")
        currency = payload.get("currency", "EUR")
        source = payload.get("source", "BUNQ_REDIRECT")
        tx_id = payload.get("tx_id", "UNKNOWN")

        logger.info(
            "💰 [FIAT_STREAM] Detected %s %s from %s [TX:%s]", amount, currency, source, tx_id
        )

        meta = {
            "oracle": "fiat_oracle_v1.3.0",
            "amount": amount,
            "currency": currency,
            "provider": "bunq",
            "tx_id": tx_id,
        }

        last_error = None
        for attempt in range(MAX_RETRIES):
            try:
                if hasattr(self.engine, "store"):
                    await self.engine.store(
                        project="cortex-revenue",
                        content=f"Received {amount} {currency} via {source}",
                        fact_type="fiat_transaction",
                        meta=meta,
                    )
                return
            except (OSError, ValueError, RuntimeError) as e:
                import secrets

                rng = secrets.SystemRandom()
                last_error = e
                delay = (BASE_BACKOFF**attempt) + rng.uniform(0.1, 1.618 ** (attempt + 1))
                logger.error(
                    "⚙️ [FIAT_ORACLE] DB lock o error: %s. Reintento %s/%s en %.2fs",
                    e,
                    attempt + 1,
                    MAX_RETRIES,
                    delay,
                )
                await asyncio.sleep(delay)

        logger.critical(
            "💀 [FIAT_ORACLE] Falla catastrófica almacenando TX %s tras %s intentos.",
            tx_id,
            MAX_RETRIES,
        )
        raise CortexError(f"FiatOracle DB Failure: {last_error}") from last_error

