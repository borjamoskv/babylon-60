"""Genesis Template Renderers for Data Definitions (SQL & YAML)."""

from __future__ import annotations

import textwrap

from cortex.extensions.genesis.models import ComponentSpec


def _render_agent_yaml(system_name: str, comp: ComponentSpec) -> str:
    """Render a YAML agent definition."""
    title = system_name.replace("_", " ").title()
    description = comp.docstring or f"Sovereign agent: {title}"
    return textwrap.dedent(f"""\
        name: "{title}"
        model: "gemini-2.5-pro"
        system_prompt: |
          You are {title}, a sovereign CORTEX agent.
          {description}

        memory:
          art_rho: 0.95
          pruning_threshold: 0.1
          retrieval_band: gamma
          tier: hot

        guardrails:
          max_session_tokens: 100000
          warn_threshold: 0.8
          max_turns: 50

        tools: []
    """)


def _render_migration(system_name: str, comp: ComponentSpec) -> str:
    """Render a SQL migration script."""
    table = comp.name.replace("-", "_")
    docstring = comp.docstring or f"Migration for {system_name}.{table}."
    return textwrap.dedent(f"""\
        -- {docstring}
        -- Auto-generated by Genesis Engine

        CREATE TABLE IF NOT EXISTS {table} (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            created_at TEXT NOT NULL DEFAULT (datetime('now')),
            updated_at TEXT NOT NULL DEFAULT (datetime('now'))
        );

        CREATE INDEX IF NOT EXISTS idx_{table}_created
            ON {table}(created_at);
    """)
