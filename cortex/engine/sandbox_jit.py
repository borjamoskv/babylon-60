"""
[C5-REAL] Sandbox JIT for OUROBOROS (Sovereign AST Cage).
Isolates mutated ASTs generated by agents, preventing host destruction.
"""

import ast
import logging
from typing import Any

logger = logging.getLogger(__name__)


class JITSandboxViolation(Exception):
    """Raised when an agent attempts to execute an illegal AST node."""

    pass


class ASTSecurityScanner(ast.NodeVisitor):
    """
    Scans an AST for forbidden nodes or attributes (e.g., eval, exec, __import__, os).
    """

    FORBIDDEN_NAMES = {
        "eval",
        "exec",
        "__import__",
        "open",
        "getattr",
        "setattr",
        "delattr",
        "compile",
    }
    FORBIDDEN_IMPORTS = {"os", "sys", "subprocess", "socket", "requests", "shutil"}

    def visit_Call(self, node: ast.Call) -> None:
        if isinstance(node.func, ast.Name) and node.func.id in self.FORBIDDEN_NAMES:
            raise JITSandboxViolation(f"Llamada a función prohibida detectada: {node.func.id}")
        if isinstance(node.func, ast.Attribute) and node.func.attr.startswith("__"):
            raise JITSandboxViolation(f"Atributo Dunder prohibido: {node.func.attr}")
        self.generic_visit(node)

    def visit_Import(self, node: ast.Import) -> None:
        for alias in node.names:
            if alias.name.split(".")[0] in self.FORBIDDEN_IMPORTS:
                raise JITSandboxViolation(f"Importación prohibida: {alias.name}")
        self.generic_visit(node)

    def visit_ImportFrom(self, node: ast.ImportFrom) -> None:
        if node.module and node.module.split(".")[0] in self.FORBIDDEN_IMPORTS:
            raise JITSandboxViolation(f"Importación prohibida (from): {node.module}")
        self.generic_visit(node)

    def visit_Attribute(self, node: ast.Attribute) -> None:
        if node.attr.startswith("__"):
            raise JITSandboxViolation(f"Acceso a atributo dunder prohibido: {node.attr}")
        self.generic_visit(node)


class SandboxJIT:
    """
    Executes agent-generated code inside a restricted globals namespace.
    """

    def __init__(self, allowed_globals: dict[str, Any] | None = None):
        self.allowed_globals = allowed_globals or {
            "__builtins__": {
                "print": print,
                "len": len,
                "range": range,
                "str": str,
                "int": int,
                "float": float,
                "bool": bool,
                "list": list,
                "dict": dict,
                "set": set,
                "tuple": tuple,
                "isinstance": isinstance,
                "sum": sum,
                "max": max,
                "min": min,
                "abs": abs,
                "round": round,
                "Exception": Exception,
                "ValueError": ValueError,
                "TypeError": TypeError,
            }
        }

    def execute(self, code_str: str, context: dict[str, Any] | None = None) -> dict[str, Any]:
        """
        Parses the code, statically verifies the AST, and then executes it.
        """
        try:
            tree = ast.parse(code_str)
        except SyntaxError as e:
            logger.error(f"SandboxJIT SyntaxError: {e}")
            raise JITSandboxViolation(f"SyntaxError en el código propuesto: {e}")

        # 1. Escaneo estático de seguridad
        scanner = ASTSecurityScanner()
        scanner.visit(tree)

        # 2. Configurar el espacio de memoria (Memory Space)
        exec_globals = dict(self.allowed_globals)
        exec_locals = dict(context) if context else {}

        # 3. Ejecutar AST compilado
        try:
            compiled_code = compile(tree, filename="<agent_jit>", mode="exec")
            exec(compiled_code, exec_globals, exec_locals)
            return exec_locals
        except Exception as e:
            logger.error(f"SandboxJIT RuntimeException: {e}")
            raise JITSandboxViolation(f"Error de ejecución en Sandbox: {e}")
