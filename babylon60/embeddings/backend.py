from __future__ import annotations

import math
from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Any

from babylon60.crypto.hash_registry import cortex_hash_raw, cortex_hash_truncated

__all__ = [
    "EmbeddingBackend",
    "HashFallbackBackend",
    "EmbeddingMetadata",
]


@dataclass(frozen=True)
class EmbeddingMetadata:
    """Metadata stored alongside each vector for future re-embedding migrations.

    When the embedding model changes, this metadata enables targeted re-computation
    of vectors generated by obsolete backends without a full database rebuild.
    """

    backend_id: str  # Unique identifier for the backend (e.g. "onnx-minilm-l6-v2")
    model_name: str  # Human-readable model name
    dimensions: int  # Vector dimensionality
    quantization: str  # "fp32", "int8", "int4", "none"
    version_hash: str  # SHA-256 of backend_id + model_name + dimensions

    @classmethod
    def compute_version_hash(cls, backend_id: str, model_name: str, dimensions: int) -> str:
        payload = f"{backend_id}:{model_name}:{dimensions}"
        return cortex_hash_truncated(payload.encode("utf-8"), length=16)


class EmbeddingBackend(ABC):
    """Abstract interface for embedding generation.

    Any embedding provider (ONNX, Ollama, API-based, hardware-accelerated,
    or future technologies not yet invented) MUST implement this contract.

    The contract guarantees:
    1. embed() returns a normalized L2 vector of fixed dimensionality.
    2. embed_batch() is semantically equivalent to calling embed() N times.
    3. dimensions() returns a constant for the lifetime of the backend instance.
    4. metadata() returns provenance information for each generated vector.
    """

    @abstractmethod
    def embed(self, text: str) -> list[float]:
        """Generate a normalized embedding vector for a single text input.

        Args:
            text: UTF-8 text to embed.

        Returns:
            A list of floats with length == self.dimensions(),
            normalized to unit L2 norm.
        """
        ...

    @abstractmethod
    def embed_batch(self, texts: list[str]) -> list[list[float]]:
        """Generate normalized embedding vectors for a batch of texts.

        Args:
            texts: List of UTF-8 strings.

        Returns:
            A list of vectors, each with length == self.dimensions().
        """
        ...

    @abstractmethod
    def dimensions(self) -> int:
        """Return the fixed dimensionality of vectors produced by this backend."""
        ...

    @abstractmethod
    def metadata(self) -> EmbeddingMetadata:
        """Return provenance metadata for vectors generated by this backend.

        This metadata MUST be stored alongside each vector in the database
        to enable future re-embedding migrations.
        """
        ...

    def is_semantic(self) -> bool:
        """Return True if this backend produces semantically meaningful vectors.

        Hash-based fallbacks should return False. Neural model backends return True.
        This flag allows the search layer to adjust ranking algorithms accordingly.
        """
        return True


class HashFallbackBackend(EmbeddingBackend):
    """Deterministic SHA-256 projection fallback.

    Produces geometrically uniform but semantically meaningless vectors.
    Used when no neural embedding model is available.
    Cosine similarity between unrelated texts converges to zero (random baseline).
    """

    def __init__(self, dimensions: int = 384) -> None:
        self._dimensions = dimensions

    def embed(self, text: str) -> list[float]:
        if not text:
            return [0.0] * self._dimensions
        return self._hash_to_unit_vector(text)

    def embed_batch(self, texts: list[str]) -> list[list[float]]:
        return [self.embed(t) for t in texts]

    def dimensions(self) -> int:
        return self._dimensions

    def metadata(self) -> EmbeddingMetadata:
        return EmbeddingMetadata(
            backend_id="hash-sha256-fallback",
            model_name="SHA-256 Deterministic Projection",
            dimensions=self._dimensions,
            quantization="none",
            version_hash=EmbeddingMetadata.compute_version_hash(
                "hash-sha256-fallback", "sha256", self._dimensions
            ),
        )

    def is_semantic(self) -> bool:
        return False

    def _hash_to_unit_vector(self, text: str) -> list[float]:
        seed = text.encode("utf-8")
        raw = bytearray()
        counter = 0
        while len(raw) < self._dimensions:
            raw.extend(cortex_hash_raw(seed + counter.to_bytes(4, "big")))
            counter += 1
        vector = [((byte / 255.0) * 2.0) - 1.0 for byte in raw[: self._dimensions]]
        return self._normalize(vector)

    @staticmethod
    def _normalize(vector: list[float]) -> list[float]:
        norm = math.sqrt(sum(v * v for v in vector))
        if norm == 0:
            return vector
        return [v / norm for v in vector]


class ONNXBackend(EmbeddingBackend):
    """ONNX Runtime backend wrapping the existing LocalEmbedder.

    This adapter bridges the legacy LocalEmbedder to the new abstract interface.
    It preserves all existing behavior (ONNX INT8 quantization, Apple Silicon
    optimization, deterministic fallback) while conforming to the stable contract.
    """

    def __init__(self, model_name: str | None = None, device: str | None = None) -> None:
        # Lazy import to avoid circular dependencies and allow graceful degradation
        self._model_name = model_name
        self._device = device
        self._embedder: Any = None
        self._fallback = HashFallbackBackend()

    def _ensure_embedder(self) -> Any:
        if self._embedder is not None:
            return self._embedder
        try:
            from babylon60.embeddings.local import LocalEmbedder

            self._embedder = LocalEmbedder(model_name=self._model_name, device=self._device)
        except Exception:
            self._embedder = None
        return self._embedder

    def embed(self, text: str) -> list[float]:
        embedder = self._ensure_embedder()
        if embedder is None:
            return self._fallback.embed(text)
        result = embedder.embed(text)
        return result if isinstance(result, list) else list(result)

    def embed_batch(self, texts: list[str]) -> list[list[float]]:
        embedder = self._ensure_embedder()
        if embedder is None:
            return self._fallback.embed_batch(texts)
        result = embedder.embed_batch(texts)
        return [r if isinstance(r, list) else list(r) for r in result]

    def dimensions(self) -> int:
        embedder = self._ensure_embedder()
        if embedder is None:
            return self._fallback.dimensions()
        return embedder.dimension

    def metadata(self) -> EmbeddingMetadata:
        embedder = self._ensure_embedder()
        if embedder is None or getattr(embedder, "_using_fallback", False):
            return self._fallback.metadata()

        model_name = self._model_name or "all-MiniLM-L6-v2"
        return EmbeddingMetadata(
            backend_id=f"onnx-{model_name}",
            model_name=model_name,
            dimensions=self.dimensions(),
            quantization="int8",
            version_hash=EmbeddingMetadata.compute_version_hash(
                f"onnx-{model_name}", model_name, self.dimensions()
            ),
        )

    def is_semantic(self) -> bool:
        embedder = self._ensure_embedder()
        if embedder is None or getattr(embedder, "_using_fallback", False):
            return False
        return True
