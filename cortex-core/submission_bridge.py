#!/usr/bin/env python3
import json
import logging
import os
import subprocess
import sys
import time
from datetime import datetime
from pathlib import Path

logger = logging.getLogger(__name__)


class SubmissionBridge:
    def __init__(self, watch_dir="./engine-c5/targets/active"):
        self.watch_dir = Path(watch_dir)
        self.verified_pocs = []

    def log(self, msg, tier="INFO"):
        # MED-03: Migrated from print() to structured logging
        logger.info("[%s] [BRIDGE] %s", tier, msg)

    def verify_poc(self, target_path, poc_file):
        """Invoke Foundry (Forge) to verify the PoC."""
        self.log(f"Verifying PoC: {poc_file} on target {target_path}", "C5-VERIFY")

        try:
            # Command: forge test --match-path <poc_file> -vvv
            # We assume Foundry is installed and the target is a Foundry project
            result = subprocess.run(
                ["forge", "test", "--match-path", str(poc_file), "-vvv"],
                cwd=target_path,
                capture_output=True,
                text=True,
                timeout=60,
            )

            if " [PASS] " in result.stdout:
                self.log(f"PoC VERIFIED (PASS): {poc_file}", "C5-SUCCESS")
                return True, result.stdout
            else:
                self.log(f"PoC FAILED: {poc_file}", "WARN")
                return False, result.stdout
        except Exception as e:
            self.log(f"Verification Engine Error: {e}", "ERROR")
            return False, str(e)

    def generate_report(self, target_name, poc_content, logs):
        """Generate a submission-ready markdown report."""
        report = f"""# CORTEX VULNERABILITY REPORT: {target_name}
**Date:** {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}
**Agent:** SAGE_COUNCIL v7
**Status:** VERIFIED (C5-REAL)

## Executive Summary
An autonomous detection loop identified a high-severity vulnerability in the target contract. 

## Proof of Concept
```solidity
{poc_content}
```

## Execution Logs
```text
{logs[:2000]}
```

## Recommended Mitigation
[Generated by ULTRA-THINK OMEGA]
Validate state transitions and enforce strict access control on internal functions.
"""
        report_path = f"./reports/submission_{target_name}_{int(time.monotonic())}.md"
        os.makedirs("./reports", exist_ok=True)
        with open(report_path, "w") as f:
            f.write(report)
        self.log(f"Report crystallized: {report_path}", "CAPITAL-YIELD")
        return report_path

    def run_bridge_loop(self):
        self.log("Submission Bridge Active. Waiting for SAGE PoCs...")
        while True:
            # Scan active targets for new PoC files in test/fuzz/
            for target in self.watch_dir.iterdir():
                if not target.is_dir():
                    continue

                fuzz_dir = target / "test" / "fuzz"
                if not fuzz_dir.exists():
                    continue

                for poc in fuzz_dir.glob("PoC_*.sol"):
                    if poc.name in [p["name"] for p in self.verified_pocs]:
                        continue

                    success, logs = self.verify_poc(target, poc)
                    self.verified_pocs.append(
                        {"name": poc.name, "target": target.name, "success": success}
                    )

                    if success:
                        with open(poc) as f:
                            poc_content = f.read()
                        self.generate_report(target.name, poc_content, logs)

            time.sleep(30)  # noqa: ASYNC101 — standalone CLI script, no event loop


if __name__ == "__main__":
    bridge = SubmissionBridge()
    try:
        bridge.run_bridge_loop()
    except KeyboardInterrupt:
        logger.info("Bridge Offline.")
